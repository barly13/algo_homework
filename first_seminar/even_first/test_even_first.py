import pytest

from typing import List

from even_first import even_first


@pytest.mark.parametrize("nums, expected", [
    # Список с четными и нечетными числами
    ([3, 1, 2, 4], [2, 4, 3, 1]),
    # Список только с четными числами
    ([2, 4, 6], [2, 4, 6]),
    # Список только с нечетными числами
    ([1, 3, 5], [1, 3, 5]),
    # Пустой список
    ([], []),
    # Список из одного четного числа
    ([2], [2]),
    # Список из одного нечетного числа
    ([1], [1]),
    # Список с отрицательными числами
    ([-1, -2, -3, -4], [-2, -4, -3, -1]),
    # Список с нулем (ноль считается четным)
    ([0, 1, 2, 3], [0, 2, 1, 3]),
])
def test_even_first(nums: List[int], expected: List[int]):
    assert even_first(nums) == expected
