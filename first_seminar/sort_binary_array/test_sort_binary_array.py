from typing import List

import pytest

from sort_binary_array import sort_binary_array


@pytest.mark.parametrize("nums, expected", [
    # Базовый случай
    ([1, 0, 1, 0, 1, 0], [0, 0, 0, 1, 1, 1]),
    # Все элементы уже на своих местах
    ([0, 0, 0, 1, 1, 1], [0, 0, 0, 1, 1, 1]),
    # Все элементы — нули
    ([0, 0, 0], [0, 0, 0]),
    # Все элементы — единицы
    ([1, 1, 1], [1, 1, 1]),
    # Пустой список
    ([], []),
    # Список из одного нуля
    ([0], [0]),
    # Список из одной единицы
    ([1], [1]),
    # Список с чередующимися нулями и единицами
    ([0, 1, 0, 1, 0, 1], [0, 0, 0, 1, 1, 1]),
    # Список с единицами в начале
    ([1, 1, 0, 0], [0, 0, 1, 1]),
    # Список с нулями в конце
    ([1, 0, 0, 0], [0, 0, 0, 1]),
])
def test_sort_binary_array(nums: List[int], expected: List[int]):
    assert sort_binary_array(nums) == expected
